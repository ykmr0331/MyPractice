package chap01;

public class J230519_I_EscapeExample {

	public static void main(String[] args) {
		System.out.println("번호\t이름\t직업");// \t는 탭만큼 띄우는거
		System.out.println("행 단위 출력\n");// \n은 줄바꿈
		System.out.println("행 단위 출력\n");
		System.out.println("우리는 \"개발자\"입니다.");// \"는 문자열 안에서 "로 표현된다.
		System.out.println("봄\\여름\\가을\\겨울"); // \\은 문자열 안에서 \임

	}

}
